package com.gpslab.kaun.model;

public class GetSpamCall {
    private String spam_call_id;
    private String spam_call_number;
    private String spam_call_name;
    private String spam_call_count;
    private String spam_call_usally_calls;
    private String spam_call_call_activity;


    public String getSpam_call_id() {
        return spam_call_id;
    }

    public void setSpam_call_id(String spam_call_id) {
        this.spam_call_id = spam_call_id;
    }

    public String getSpam_call_number() {
        return spam_call_number;
    }

    public void setSpam_call_number(String spam_call_number) {
        this.spam_call_number = spam_call_number;
    }

    public String getSpam_call_name() {
        return spam_call_name;
    }

    public void setSpam_call_name(String spam_call_name) {
        this.spam_call_name = spam_call_name;
    }

    public String getSpam_call_count() {
        return spam_call_count;
    }

    public void setSpam_call_count(String spam_call_count) {
        this.spam_call_count = spam_call_count;
    }

    public String getSpam_call_usally_calls() {
        return spam_call_usally_calls;
    }

    public void setSpam_call_usally_calls(String spam_call_usally_calls) {
        this.spam_call_usally_calls = spam_call_usally_calls;
    }

    public String getSpam_call_call_activity() {
        return spam_call_call_activity;
    }

    public void setSpam_call_call_activity(String spam_call_call_activity) {
        this.spam_call_call_activity = spam_call_call_activity;
    }
}

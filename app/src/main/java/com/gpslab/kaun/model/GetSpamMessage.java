package com.gpslab.kaun.model;

public class GetSpamMessage {
    private String spam_msg_id;
    private String spam_msg_number;
    private String spam_msg_name;
    private String spam_msg_count;

    public String getSpam_msg_id() {
        return spam_msg_id;
    }

    public void setSpam_msg_id(String spam_msg_id) {
        this.spam_msg_id = spam_msg_id;
    }

    public String getSpam_msg_number() {
        return spam_msg_number;
    }

    public void setSpam_msg_number(String spam_msg_number) {
        this.spam_msg_number = spam_msg_number;
    }

    public String getSpam_msg_name() {
        return spam_msg_name;
    }

    public void setSpam_msg_name(String spam_msg_name) {
        this.spam_msg_name = spam_msg_name;
    }

    public String getSpam_msg_count() {
        return spam_msg_count;
    }

    public void setSpam_msg_count(String spam_msg_count) {
        this.spam_msg_count = spam_msg_count;
    }
}

package com.gpslab.kaun.model;

public class GetSMSTable {
    public String sms_key;
    public String sms_url;
    public String sms_name;

    public String ids;


    public String getIds() {
        return ids;
    }

    public void setIds(String ids) {
        this.ids = ids;
    }

    public String getSms_key() {
        return sms_key;
    }

    public void setSms_key(String sms_key) {
        this.sms_key = sms_key;
    }

    public String getSms_url() {
        return sms_url;
    }

    public void setSms_url(String sms_url) {
        this.sms_url = sms_url;
    }

    public String getSms_name() {
        return sms_name;
    }

    public void setSms_name(String sms_name) {
        this.sms_name = sms_name;
    }
}
